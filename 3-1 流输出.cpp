#include <iostream>

using namespace std;

int main()
{
    //字符变量的声明
    char first = 'Y',
         mid = 'a',
         last = 'n';
    //整型变量的声明
    int hour = 8;
    //输出
    cout << "Hello, " << first << mid << last << '!' << endl;
    cout << "It's " << hour << " o'clock." << endl;
    
    return 0;
}
